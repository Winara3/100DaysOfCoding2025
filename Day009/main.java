package Day009;
public class main {
    public static void main(String[] args) {
        //Deklarasi variable string
        String nama;

        //Insilisasi nama awal
        nama = "winara";
        System.out.println("nama awal: " + nama);

        //Update nilai variabel
        nama = "anggun";
        System.out.println("nama setelah update: " + nama); 

        //Update lagi
        nama = nama + " purnama"; //menambahkan teks variabel
        System.out.println("nama setelah ditambah: " + nama);
    }
    
}
