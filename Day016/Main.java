package Day016;

public class Main {
  public static void main(String[] args) {
    int a = 10;
    int b = 3;

    // Perkalian
    int hasilkali = a * b;
    System.out.println("Hasil perkalian: " + hasilkali);

    // pembagian Int
    int hasilbagi1 = a / b;
    System.out.println("Hasil Bagi (Int): " + hasilbagi1);

    // Pembagian Double
    double hasilbagi2 = a / b;
    System.out.println("Hasil Bagi (Double): " + hasilbagi2);

  }

}