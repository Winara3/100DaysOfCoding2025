package Day004;

public class main {
    public static void main(String[] args) {
        System.out.println("Hello\nWorld!");   // \n membuat baris baru
        System.out.println("Nama :\tWinara");   // \t membuat tab
        System.out.println("Umurnya\b :18");   // \t membuat tab
        System.out.println("Hobby: \"Singing\""); // \" untuk tanda kutip
        System.out.println("Have nice day: \\");   // \\ menampilkan backslash
        System.out.println("Unicode: \u2764"); // ‚ù§
    }
    
}
